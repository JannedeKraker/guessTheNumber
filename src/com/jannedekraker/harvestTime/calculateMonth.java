package com.jannedekraker.harvestTime;


public class calculateMonth {
    public static String calcMonth(String answerQuestionOne){
        if (answerQuestionOne.equals("January") ) {
            final String harvestMonth = "June";
           return harvestMonth;
        }
        else if (answerQuestionOne.equals("February")) {
            final String harvestMonth = "July";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("March")) {
            final String harvestMonth = "August";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("April")) {
            final String harvestMonth = "September";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("May")) {
            final String harvestMonth = "October";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("June"))  {
            final String harvestMonth = "November";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("July")) {
            final String harvestMonth = "December";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("August")) {
            final String harvestMonth = "January";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("September")) {
            final String harvestMonth = "February";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("October")) {
            final String harvestMonth = "March";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("November")) {
            final String harvestMonth = "April";
            return harvestMonth;
        }
        else if (answerQuestionOne.equals("December")) {
            final String harvestMonth = "May";
            return harvestMonth;
        }

        return "";
    }
}
